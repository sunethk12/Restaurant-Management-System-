    void autonumber() {
        try {
            Connection con = DB.connect();
            ResultSet r = con.createStatement().executeQuery("select*from user");
            int max = 0;
            while (r.next()) {
                int id = Integer.parseInt(r.getString("user_id").trim());
                if (max < id) {
                    max = id;
                }
            }

            jTextField1.setText(max + 1 + " ");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    void setList() {
        try {

            Connection con = DB.connect();
            ResultSet r = con.createStatement().executeQuery("select *from user");
            Vector v = new Vector();
            while (r.next()) {
                String id = r.getString("user_id");
                String fn = r.getString("f_name");
                String ln = r.getString("l_name");
                String b = id + "  " + fn + "  " + ln;
                v.add(b);
            }
            jList1.setListData(v);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

